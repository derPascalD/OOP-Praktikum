public class Artikel {
    private int artikelNummer;
    private double nettoPreis;

    Artikel(int artikelNummer, double nettoPreis) {
        this.artikelNummer = artikelNummer;
        this.nettoPreis = nettoPreis;
    }

    double getPreis() {
        return nettoPreis;
    }

    String getInfo() {
        return getInfo();
    }
}
